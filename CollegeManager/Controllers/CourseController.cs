using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Threading.Tasks;
using System.Net;
using System.Web;
using System.Web.Mvc;
using CollegeManager.Data.Entities;
using CollegeManager.Data.Persistance;
using CollegeManager.ViewModel;

namespace CollegeManager.Controllers
{
    public class CourseController : Controller
    {
        private CollegeDbContext db = new CollegeDbContext();

        // GET: Courses
        public async Task<ActionResult> Index()
        {
            string query = @"SELECT 
                 Courses.CourseId as CourseId,
                 Courses.Title as Title,
                 COUNT(Teachers.TeacherId) as TeacherCount,
                 COUNT(Students.StudentId) as StudentCount
            FROM Subjects
                INNER JOIN Teachers ON(Subjects.SubjectId = Teachers.SubjectId)
                LEFT JOIN Courses ON(Subjects.CourseId = Courses.CourseId)
                LEFT JOIN Students ON(Subjects.SubjectId = Students.SubjectId)
            GROUP BY Courses.Title, Courses.CourseId";

            var course = await db.Database.SqlQuery<CourseViewModel>(query).ToListAsync();

            return View(course);
        }

        // GET: Courses/Details/5
        public async Task<ActionResult> Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Course course = await db.Courses.FindAsync(id);
            if (course == null)
            {
                return HttpNotFound();
            }
            return View(course);
        }

        // GET: Courses/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Courses/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Create([Bind(Include = "Title")] CourseViewModel courseViewModel)
        {
            var course = new Course(courseViewModel.Title);

            if (ModelState.IsValid)
            {
                db.Courses.Add(course);
                await db.SaveChangesAsync();
                return RedirectToAction("Index");
            }

            return View(course);
        }

        // GET: Courses/Edit/5
        public async Task<ActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Course course = await db.Courses.FindAsync(id);
            if (course == null)
            {
                return HttpNotFound();
            }
            return View(course);
        }

        // POST: Courses/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Edit([Bind(Include = "CourseId,Title")] CourseViewModel courseViewModel)
        {
            var course = new Course(courseViewModel.Title)
            {
                CourseId = courseViewModel.CourseId ?? default(int)
            };

            if (ModelState.IsValid)
            {
                db.Entry(course).State = EntityState.Modified;
                await db.SaveChangesAsync();
                return RedirectToAction("Index");
            }
            return View(course);
        }

        // GET: Courses/Delete/5
        public async Task<ActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Course course = await db.Courses.FindAsync(id);
            if (course == null)
            {
                return HttpNotFound();
            }
            return View(course);
        }

        // POST: Courses/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> DeleteConfirmed(int id)
        {
            Course course = await db.Courses.FindAsync(id);
            db.Courses.Remove(course);
            await db.SaveChangesAsync();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
